package com.javastruts.pojo;

public class Mensagem {
	
   private String status;
	
   private String messagemEnviada;
	
   private String idioma;
   

public String getStatus() {
	return status;
}

public void setStatus(String status) {
	this.status = status;
}

public String getMessagemEnviada() {
	return messagemEnviada;
}

public void setMessagemEnviada(String messagemEnviada) {
	this.messagemEnviada = messagemEnviada;
}

public String getIdioma() {
	return idioma;
}

public void setIdioma(String idioma) {
	this.idioma = idioma;
}
   
   
	

}
